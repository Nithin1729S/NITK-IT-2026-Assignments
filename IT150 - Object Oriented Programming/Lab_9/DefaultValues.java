public class DefaultValues {
    static byte byteDefault;
    static short shortDefault;
    static int intDefault;
    static long longDefault;
    static float floatDefault;
    static double doubleDefault;
    static char charDefault;
    static boolean booleanDefault;

    public static void main(String[] args) {
        System.out.println("Default value of byte: " + byteDefault);
        System.out.println("Default value of short: " + shortDefault);
        System.out.println("Default value of int: " + intDefault);
        System.out.println("Default value of long: " + longDefault);
        System.out.println("Default value of float: " + floatDefault);
        System.out.println("Default value of double: " + doubleDefault);
        System.out.println("Default value of char: " + charDefault);
        System.out.println("Default value of boolean: " + booleanDefault);
    }
}
